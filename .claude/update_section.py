# Script to update 理解陌生代码 section
file_path = '工具指南/Claude Code 高级使用指南.md'

with open(file_path, 'r', encoding='utf-8') as f:
    content = f.read()

old_marker = '### 理解陌生代码\n<!-- 待填充 -->'

new_section = r'''### 理解陌生代码

#### 1. 基础解法

最直接的方法是让 Claude 逐行解释代码。适合场景：接手遗留代码、阅读开源库、理解同事写的复杂逻辑。

**提问示例：**

```
请逐行解释这段代码的作用，重点说明每个函数的输入输出和副作用。

[粘贴代码]
```

```
这里的 reduce 调用是什么意思？为什么初始值是空对象？
```

```
这段代码的核心逻辑是什么？用一句话总结它在做什么。
```

适用场景：代码片段较短（< 200行）、问题明确、不需要理解跨文件依赖。

---

#### 2. 高级技巧

**技巧1：用 @file 引用建立完整上下文**

当陌生代码涉及多个文件时，单独粘贴片段往往无法理解全貌。使用 `@文件名` 引用让 Claude 同时看到相关文件。

```bash
# 引用多个相关文件
@src/auth/middleware.ts @src/types/user.ts
"这个中间件的鉴权逻辑是怎么工作的？token 是从哪里来的？"

# 引用入口文件理解整体结构
@src/index.ts @src/router.ts
"从入口开始，请梳理一个请求的完整处理链路。"
```

**技巧2：用分层提问管理 Context 复杂度**

面对大型陌生代码库时，采用「先宏观后微观」的分层策略，每次对话聚焦一个层次。

```bash
# 第一轮：理解整体架构（保持 context 干净）
"这个项目的目录结构是怎样的？各个模块分别负责什么？"

# 第二轮（新对话）：深入某个模块
@src/payment/ "支付模块的核心流程是什么？从用户点击支付到回调完成，经过哪些关键步骤？"

# 第三轮：聚焦具体问题
@src/payment/processor.ts "processPayment 函数中，为什么要在第47行做两次重试？这个设计意图是什么？"
```

**技巧3：在 CLAUDE.md 中预设代码库背景**

如果你长期需要理解某个代码库，在项目根目录创建 `CLAUDE.md`，让 Claude 每次都带着正确背景知识读代码。

```markdown
## 项目背景
这是一个电商后台服务，基于 NestJS + TypeORM，数据库是 PostgreSQL。

## 核心概念
- Order：订单实体，状态机驱动（pending → paid → shipped → completed）
- SKU：库存单元，与 Product 是多对一关系
- 所有金额字段单位为「分」，避免浮点数精度问题

## 代码约定
- `*Service` 文件包含业务逻辑
- `*Repository` 文件包含数据库操作
- 错误统一用 `AppException` 抛出，不要用原生 Error

## 注意事项
- legacy/ 目录下是旧代码，逻辑可能有问题，不要作为参考
- 凡是有 TODO(tech-debt) 注释的地方都是已知技术债
```

**技巧4：让 Claude 生成代码地图**

对于完全陌生的大型代码库，先让 Claude 生成一份「代码地图」作为后续提问的索引。

```bash
# 生成函数调用关系
@src/ "请列出这个模块中所有公开函数及其调用关系，用树状结构展示。"

# 识别关键路径
"如果我要修改用户注册逻辑，哪些文件会受到影响？请列出需要关注的文件清单。"

# 生成数据流向描述
"请描述数据从 API 入口到数据库写入的完整流向，标出每个环节做了什么转换。"
```

---

#### 3. 最佳实践

- **先问「做什么」，再问「怎么做」**：先理解函数的业务意图，再理解实现细节。直接问实现细节容易迷失在代码噪音中。

- **带着具体问题读代码**：「这个函数为什么要加锁？」比「解释这段代码」能得到更有价值的回答。泛泛的问题得到泛泛的答案。

- **说出你的困惑点**：「这里用了 Proxy，我不太熟悉这个模式，请结合这段代码解释它解决了什么问题」比「解释 Proxy」更有针对性。

- **顺带问「危险区域」**：理解陌生代码时，附带问「这段代码有哪些潜在风险或容易踩坑的地方？」提前了解雷区。

- **用角色扮演获得更深层解释**：「请以这段代码的原作者身份解释，你当时为什么这样设计，有没有考虑过其他方案？」有时能得到更有洞察力的分析。

- **常见陷阱——Context 污染**：在同一对话里理解多段无关代码，会导致 Claude 混淆上下文。理解不同模块时，开启新会话保持 context 干净。

- **常见陷阱——过度信任解释**：Claude 可能对代码意图做出「合理但错误」的推断，特别是当代码本身有 bug 或违反常规模式时。对关键逻辑，用测试或实际运行验证 Claude 的解释。'''

if old_marker in content:
    content = content.replace(old_marker, new_section)
    with open(file_path, 'w', encoding='utf-8') as f:
        f.write(content)
    print('SUCCESS: File updated')
else:
    print('ERROR: Marker not found')
    print('Looking for:', repr(old_marker))
